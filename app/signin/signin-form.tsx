"use client";

import { useRouter } from "next/navigation";
import { useState } from "react";

import { Button } from "@/components/ui/Button";
import { Input } from "@/components/ui/Input";
import { createClient } from "@/lib/supabase/client";

import {
  signInWithMagicLink,
  signInWithPassword,
  signUpWithPassword,
} from "./actions";

type AuthTab = "signin" | "signup" | "magic-link";

export function SignInForm() {
  const router = useRouter();
  const [tab, setTab] = useState<AuthTab>("signin");
  const [email, setEmail] = useState("");
  const [password, setPassword] = useState("");
  const [confirmPassword, setConfirmPassword] = useState("");
  const [isLoading, setIsLoading] = useState(false);
  const [message, setMessage] = useState<{
    type: "success" | "error";
    text: string;
  } | null>(null);

  function handleOAuth(provider: "github" | "google") {
    const supabase = createClient();
    const siteUrl = process.env.NEXT_PUBLIC_SITE_URL ?? "http://localhost:3000";
    supabase.auth.signInWithOAuth({
      provider,
      options: {
        redirectTo: `${siteUrl}/auth/callback`,
      },
    });
  }

  async function handleSubmit(e: React.FormEvent) {
    e.preventDefault();
    setIsLoading(true);
    setMessage(null);

    if (tab === "magic-link") {
      const result = await signInWithMagicLink(email);
      if (result.error) {
        setMessage({ type: "error", text: result.error });
      } else {
        setMessage({
          type: "success",
          text: "Check your email for the magic link!",
        });
        setEmail("");
      }
    } else if (tab === "signup") {
      if (password !== confirmPassword) {
        setMessage({ type: "error", text: "Passwords do not match" });
        setIsLoading(false);
        return;
      }
      const result = await signUpWithPassword(email, password);
      if (result.error) {
        setMessage({ type: "error", text: result.error });
      } else {
        setMessage({
          type: "success",
          text: "Check your email to confirm your account!",
        });
        setEmail("");
        setPassword("");
        setConfirmPassword("");
      }
    } else {
      const result = await signInWithPassword(email, password);
      if (result.error) {
        setMessage({ type: "error", text: result.error });
      } else {
        router.push("/");
        router.refresh();
      }
    }

    setIsLoading(false);
  }

  function switchTab(newTab: AuthTab) {
    setTab(newTab);
    setMessage(null);
    setPassword("");
    setConfirmPassword("");
  }

  return (
    <div className="space-y-6">
      {/* OAuth buttons */}
      <div className="space-y-3">
        <Button
          type="button"
          variant="secondary"
          className="w-full"
          onClick={() => handleOAuth("github")}
          disabled={isLoading}
          icon={<GitHubIcon />}
        >
          Continue with GitHub
        </Button>
        <Button
          type="button"
          variant="secondary"
          className="w-full"
          onClick={() => handleOAuth("google")}
          disabled={isLoading}
          icon={<GoogleIcon />}
        >
          Continue with Google
        </Button>
      </div>

      {/* Divider */}
      <div className="relative">
        <div className="absolute inset-0 flex items-center">
          <span className="w-full border-t border-border" />
        </div>
        <div className="relative flex justify-center text-caption">
          <span className="bg-surface-secondary px-2 text-content-tertiary">
            or continue with email
          </span>
        </div>
      </div>

      {/* Tabs */}
      <div className="flex rounded-lg border border-border bg-surface-sunken p-1">
        <button
          type="button"
          onClick={() => switchTab("signin")}
          className={`flex-1 rounded-md px-3 py-1.5 text-body-sm font-medium transition-colors ${
            tab === "signin"
              ? "bg-surface-secondary text-content-primary shadow-sm"
              : "text-content-tertiary hover:text-content-secondary"
          }`}
        >
          Sign In
        </button>
        <button
          type="button"
          onClick={() => switchTab("signup")}
          className={`flex-1 rounded-md px-3 py-1.5 text-body-sm font-medium transition-colors ${
            tab === "signup"
              ? "bg-surface-secondary text-content-primary shadow-sm"
              : "text-content-tertiary hover:text-content-secondary"
          }`}
        >
          Sign Up
        </button>
        <button
          type="button"
          onClick={() => switchTab("magic-link")}
          className={`flex-1 rounded-md px-3 py-1.5 text-body-sm font-medium transition-colors ${
            tab === "magic-link"
              ? "bg-surface-secondary text-content-primary shadow-sm"
              : "text-content-tertiary hover:text-content-secondary"
          }`}
        >
          Magic Link
        </button>
      </div>

      {/* Form */}
      <form onSubmit={handleSubmit} className="space-y-4">
        <Input
          type="email"
          label="Email address"
          value={email}
          onChange={(e) => setEmail(e.target.value)}
          placeholder="you@example.com"
          required
          disabled={isLoading}
          autoComplete="email"
          inputMode="email"
        />

        {tab !== "magic-link" && (
          <Input
            type="password"
            label="Password"
            value={password}
            onChange={(e) => setPassword(e.target.value)}
            placeholder="At least 6 characters"
            required
            disabled={isLoading}
            autoComplete={
              tab === "signup" ? "new-password" : "current-password"
            }
          />
        )}

        {tab === "signup" && (
          <Input
            type="password"
            label="Confirm password"
            value={confirmPassword}
            onChange={(e) => setConfirmPassword(e.target.value)}
            placeholder="Repeat your password"
            required
            disabled={isLoading}
            autoComplete="new-password"
          />
        )}

        {message && (
          <div
            className={`rounded-md border p-3 text-sm ${
              message.type === "success"
                ? "border-green-200 bg-green-50 text-green-700 dark:border-green-800 dark:bg-green-900/20 dark:text-green-400"
                : "border-red-200 bg-red-50 text-red-700 dark:border-red-800 dark:bg-red-900/20 dark:text-red-400"
            }`}
          >
            {message.text}
          </div>
        )}

        <Button
          type="submit"
          className="w-full"
          loading={isLoading}
          disabled={
            !email ||
            (tab !== "magic-link" && !password) ||
            (tab === "signup" && !confirmPassword)
          }
        >
          {tab === "magic-link"
            ? "Send Magic Link"
            : tab === "signup"
              ? "Create Account"
              : "Sign In"}
        </Button>

        {tab === "magic-link" && (
          <p className="text-center text-caption text-content-tertiary">
            We&apos;ll send you an email with a link to sign in. No password
            required.
          </p>
        )}
      </form>
    </div>
  );
}

